import 'dart:convert';
import 'package:eat_easy_assignment/features/movies/domain/entities/movie_cast_entity.dart';

MovieCast movieCastFromJson(String str) => MovieCast.fromJson(json.decode(str));

String movieCastToJson(MovieCast data) => json.encode(data.toJson());

class MovieCast {
  int? id;
  List<Cast>? cast;

  MovieCast({
    this.id,
    this.cast,
  });

  factory MovieCast.fromJson(Map<String, dynamic> json) => MovieCast(
        id: json["id"],
        cast: json["cast"] == null
            ? []
            : List<Cast>.from(json["cast"]!.map((x) => Cast.fromJson(x))),
      );

  Map<String, dynamic> toJson() => {
        "id": id,
        "cast": cast == null
            ? []
            : List<dynamic>.from(cast!.map((x) => x.toJson())),
      };

  // Converts MovieCast to MovieCastEntity (domain model)
  MovieCastEntity toEntity() {
    return MovieCastEntity(
      id: id,
      cast: cast?.map((cast) => cast.toEntity()).toList(),
    );
  }
}
// cast.dart

class Cast {
  bool? adult;
  int? gender;
  int? id;
  String? knownForDepartment;
  String? name;
  String? originalName;
  double? popularity;
  String? profilePath;
  int? castId;
  String? character;
  String? creditId;
  int? order;

  Cast({
    this.adult,
    this.gender,
    this.id,
    this.knownForDepartment,
    this.name,
    this.originalName,
    this.popularity,
    this.profilePath,
    this.castId,
    this.character,
    this.creditId,
    this.order,
  });

  factory Cast.fromJson(Map<String, dynamic> json) => Cast(
        adult: json["adult"],
        gender: json["gender"],
        id: json["id"],
        knownForDepartment: json["known_for_department"],
        name: json["name"],
        originalName: json["original_name"],
        popularity: json["popularity"]?.toDouble(),
        profilePath: json["profile_path"],
        castId: json["cast_id"],
        character: json["character"],
        creditId: json["credit_id"],
        order: json["order"],
      );

  Map<String, dynamic> toJson() => {
        "adult": adult,
        "gender": gender,
        "id": id,
        "known_for_department": knownForDepartment,
        "name": name,
        "original_name": originalName,
        "popularity": popularity,
        "profile_path": profilePath,
        "cast_id": castId,
        "character": character,
        "credit_id": creditId,
        "order": order,
      };

  // Converts Cast to CastEntity (domain model)
  CastEntity toEntity() {
    return CastEntity(
      adult: adult,
      gender: gender,
      id: id,
      knownForDepartment: knownForDepartment,
      name: name,
      originalName: originalName,
      popularity: popularity,
      profilePath: profilePath,
      castId: castId,
      character: character,
      creditId: creditId,
      order: order,
    );
  }
}
